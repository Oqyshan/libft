#include "libft.h"

int	ft_isalnum(int nb)
{
	if ((nb >= 'a' && nb <= 'z') || (nb >= 'A' && nb <= 'Z'))
		return (1);
	if (nb >= '0' && nb <= '9')
		return (1);
	return (0);
}
